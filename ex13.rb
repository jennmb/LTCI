# sets three variables as arguments
# ARGV unpacks arguments into three variables
# args. input by user
first, second, third = ARGV
# ARGV "argument variable" holds the argument you pass to the script when you run it

# inputs Your first variable is: (first variable value)
puts "Your first variable is: #{first}"
# inputs Your second variable is: (second variable value)
puts "Your second variable is: #{second}"
# inputs Your third variable is: (third variable value)
puts "Your third variable is: #{third}"

# must type in arguments when running script or it will not work